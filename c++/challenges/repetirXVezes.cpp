#include <bits/stdc++.h>

using namespace std;

// Repetir X Vezes

// Faça um programa que leia um inteiro XX e imprima XX vezes a mensagem "NepsAcademy eh Sucesso".
// Entrada

// A entrada consiste de uma linha contendo um inteiro XX.
// Saída

// A saída consiste de XX linhas, cada uma contendo a mensagem indicada no enunciado.
// Restrições

//     1≤X≤1001≤X≤100

// Exemplos de Entrada

int main(){
   int n,i = 0;
   cin >> n;
   while (i < n)
   {
    cout << "NepsAcademy eh Sucesso" << '\n';
    i++;
   }
   
    return 0;
}